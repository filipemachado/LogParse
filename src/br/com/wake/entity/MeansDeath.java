package br.com.wake.entity;

public class MeansDeath {
	
	private int indexMeansDeath;
	private String meansDeath;
	private int totalMeansDeath;
	
	
	
	public MeansDeath() {
		
	}



	/**
	 * @return the indexMeansDeath
	 */
	public int getIndexMeansDeath() {
		return indexMeansDeath;
	}



	/**
	 * @param indexMeansDeath the indexMeansDeath to set
	 */
	public void setIndexMeansDeath(int indexMeansDeath) {
		this.indexMeansDeath = indexMeansDeath;
	}



	/**
	 * @return the meansDeath
	 */
	public String getMeansDeath() {
		return meansDeath;
	}



	/**
	 * @param meansDeath the meansDeath to set
	 */
	public void setMeansDeath(String meansDeath) {
		this.meansDeath = meansDeath;
	}



	/**
	 * @return the totalMeansDeath
	 */
	public int getTotalMeansDeath() {
		return totalMeansDeath;
	}



	/**
	 * @param totalMeansDeath the totalMeansDeath to set
	 */
	public void setTotalMeansDeath(int totalMeansDeath) {
		this.totalMeansDeath = totalMeansDeath;
	}
	
	
	
	

}
